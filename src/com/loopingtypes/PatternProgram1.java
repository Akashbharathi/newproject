package com.loopingtypes;   /// NESTED IF

public class PatternProgram1 {
	
	public static void main(String args[]) {
		

		System.out.println("!!! PATTERN PROGRAM STAR METHOD !!!");
		    for (int i = 1; i <=10; i++) {
			
			    for (int j = 1; j <=i; j++) {
				System.out.print("*");
				}
			    
			System.out.println();
		}
//////////////////////////////////////////////////////////////////////////////////////////
		
		System.out.println("!!! PATTERN PROGRAM NUMBER METHOD !!!");
            
		 for (int i = 1; i <=10; i++) {
			
			for (int j = 1; j <i; j++) {
				
				System.out.print(j);
				}
			
			System.out.println();
		   }
		
		}
		
	}


