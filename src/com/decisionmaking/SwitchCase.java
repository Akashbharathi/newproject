package com.decisionmaking;

public class SwitchCase {
	
	

	public static void main(String[] args) {
		
		
		switch (1) {
		
		case 1:
			System.out.println("akash");
			
			break;
			
		case 2:
			System.out.println("nithi");
			break;
		case 3:
			System.out.println("karthick bro");

		default :
			System.out.println("names");
			break;
		}
		
	}

}
